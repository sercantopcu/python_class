def convert_to_list(PARAMETER1):
    PARAMETER1 = list(PARAMETER1)
    print(PARAMETER1)



def convert_to_tuple(PARAMETER1):
    PARAMETER1 = tuple(PARAMETER1)
    print(PARAMETER1)


def convert_to_str(PARAMETER1):
    PARAMETER1 = str(PARAMETER1)
    print(PARAMETER1)


def convert_to_dict(PARAMETER1):
    PARAMETER1 = dict(PARAMETER1)
    print(PARAMETER1)