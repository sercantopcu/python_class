# Assigning a value of "apple" into a key "FRUIT"
FRUIT = "apple"

# Shows the value of the FRUIT key
print(FRUIT)

# Shows the location of the string  in the memory
print(id(FRUIT))

# Shows the data type
print(type(FRUIT))


# Shows the methods of the string
# print(dir(FRUIT))

# Capitalizes the first letter
print(FRUIT.capitalize())

# Find how many time p repeated
print(FRUIT.count("p"))

# Find oud if the the string starts with something
print(FRUIT.startswith("app"))

# Find oud if the the string end with something
print(FRUIT.endswith("pple"))

# Change to uppercases
print(FRUIT.upper())

